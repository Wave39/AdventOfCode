    func boolArrayToInt(_ bits: [Bool]) -> Int {
        bits.reduce(0) { ($0 << 1) | ($1 ? 1 : 0) }
    }

    func intToBoolArray(_ value: Int, _ count: Int) -> [Bool] {
        let bin = (String(repeatElement("0", count: count)) + String(value, radix: 2)).suffix(count)
        return bin.map { $0 == "1" }
    }

    struct MachineForPart1: Hashable {
        var desiredStatus: [Bool] = []
        var switches: [[Int]] = []
        var joltages: [Int] = []
    }

    private func solvePart1(str: String) -> Int {
        let lines = str.parseIntoStringArray()
        var machines = [MachineForPart1]()
        for line in lines {
            let arr = line.parseIntoStringArray(separator: " ")
            var machine = MachineForPart1()
            for var str in arr {
                let prefix = str.removeFirst()
                str.removeLast()
                if prefix == "[" {
                    for c in str {
                        if c == "." {
                            machine.desiredStatus.append(false)
                        } else {
                            machine.desiredStatus.append(true)
                        }
                    }
                } else if prefix == "(" {
                    machine.switches.append(str.parseIntoIntArray(separator: ","))
                } else {
                    machine.joltages = str.parseIntoIntArray(separator: ",")
                }
            }

            machines.append(machine)
        }

        var overallButtonPresses = 0
        for machine in machines {
            let targetStatus = boolArrayToInt(machine.desiredStatus)
            var statusDict = Dictionary<Int, Int>()
            var pressCount = 0
            statusDict[0] = pressCount
            var statusesToProcessSet = Set<Int>()
            statusesToProcessSet.insert(0)
            var foundPressCount = 0
            while foundPressCount == 0 {
                var newStatusesToProcessSet = Set<Int>()
                pressCount += 1
                for statusToProcess in statusesToProcessSet {
                    let originalStatus = intToBoolArray(statusToProcess, machine.desiredStatus.count)
                    for switchToPress in machine.switches {
                        var newStatus = originalStatus
                        for buttonToPress in switchToPress {
                            newStatus[buttonToPress] = !newStatus[buttonToPress]
                        }

                        let newStatusInt = boolArrayToInt(newStatus)
                        if statusDict[newStatusInt] == nil {
                            statusDict[newStatusInt] = pressCount
                            newStatusesToProcessSet.insert(newStatusInt)
                        }
                    }
                }

                if statusDict[targetStatus] != nil {
                    foundPressCount = statusDict[targetStatus]!
                } else {
                    statusesToProcessSet = newStatusesToProcessSet
                }
            }

            overallButtonPresses += foundPressCount
        }

        return overallButtonPresses
    }

    struct MachineForPart2 {
        let goalMask: UInt32
        let goalCounters: [Int64]
        let buttonMasks: [UInt32]
    }

    func solveAll(_ machines: [MachineForPart2]) -> Int64 {
        return machines.map(solveLP).reduce(0, +)
    }

    func parse(_ input: String) -> [MachineForPart2] {
        var machines: [MachineForPart2] = []

        for rawLine in input.split(separator: "\n") {
            let line = String(rawLine)
            // Split on whitespace
            let parts = line
                .split(whereSeparator: { $0.isWhitespace })
                .map(String.init)

            guard !parts.isEmpty else { continue }

            // First part: like "[###..]" -> between chars 1..len-1
            let goalPart = parts[0]
            let goalStrStart = goalPart.index(after: goalPart.startIndex)
            let goalStrEnd = goalPart.index(before: goalPart.endIndex)
            let goalStr = String(goalPart[goalStrStart..<goalStrEnd])

            var goalMask: UInt32 = 0
            for (i, c) in goalStr.enumerated() {
                if c == "#" {
                    goalMask |= 1 << UInt32(i)
                }
            }

            // Last part: like "(1,2,3)" -> counters
            guard let lastPart = parts.last else { continue }
            let cStart = lastPart.index(after: lastPart.startIndex)
            let cEnd = lastPart.index(before: lastPart.endIndex)
            let counterStr = String(lastPart[cStart..<cEnd])

            let goalCounters: [Int64] = counterStr
                .split(separator: ",")
                .compactMap { Int64($0) }

            // Middle parts: button masks
            var buttonMasks: [UInt32] = []
            if parts.count > 2 {
                for part in parts[1..<(parts.count - 1)] {
                    var mask: UInt32 = 0
                    let inner: String
                    if part.hasPrefix("(") || part.hasPrefix("{") {
                        let s = part.index(after: part.startIndex)
                        let e = part.index(before: part.endIndex)
                        inner = String(part[s..<e])
                    } else {
                        inner = part
                    }

                    for numStr in inner.split(separator: ",") {
                        if let bit = UInt32(numStr) {
                            mask |= 1 << bit
                        }
                    }
                    buttonMasks.append(mask)
                }
            }

            machines.append(
                MachineForPart2(
                    goalMask: goalMask,
                    goalCounters: goalCounters,
                    buttonMasks: buttonMasks
                )
            )
        }

        return machines
    }

    // MARK: - Part 1 BFS

    func solveBFS(_ machine: MachineForPart2) -> Int64 {
        var queue: [(UInt32, Int64)] = []
        queue.append((0, 0))
        var head = 0

        var visited: Set<UInt32> = [0]

        while head < queue.count {
            let (curr, steps) = queue[head]
            head += 1

            if curr == machine.goalMask {
                return steps
            }

            for bMask in machine.buttonMasks {
                let nxt = curr ^ bMask
                if !visited.contains(nxt) {
                    visited.insert(nxt)
                    queue.append((nxt, steps + 1))
                }
            }
        }
        return 0
    }

    // MARK: - Simplex + ILP branch-and-bound

    private let INF: Double = .infinity
    private let EPS: Double = 1e-9

    /// Returns (optValue, optSolution) or (-INF, nil) if infeasible/unbounded
    func simplex(lhs: [[Double]], c: [Double]) -> (Double, [Double]?) {
        let m = lhs.count
        let n = lhs[0].count - 1   // includes RHS column

        // Non-basic indices: 0..n-1 then artificial -1
        var nIndices: [Int32] = (0..<Int32(n)).map { $0 }
        nIndices.append(-1)

        // Basic indices: n..n+m-1
        var bIndices: [Int32] = (Int32(n)..<Int32(n + m)).map { $0 }

        // Tableau d: size (m+2) x (n+2)
        var d = Array(
            repeating: Array(repeating: 0.0, count: n + 2),
            count: m + 2
        )

        // Copy lhs into d, with extra column n+1 = -1 for artificial
        for (i, lhsRow) in lhs.enumerated() {
            for j in 0...n {
                d[i][j] = lhsRow[j]
            }
            d[i][n + 1] = -1.0
        }

        // Swap columns n and n+1 in rows 0..m-1
        for i in 0..<m {
            d[i].swapAt(n, n + 1)
        }

        // Objective row m: c[0..n-1]
        for j in 0..<n {
            d[m][j] = c[j]
        }

        // Auxiliary objective row m+1: coefficient for artificial variables
        d[m + 1][n] = 1.0

        func pivot(_ r: Int, _ s: Int) {
            let k = 1.0 / d[r][s]

            let rows = m + 2
            let cols = n + 2

            for i in 0..<rows {
                if i == r { continue }
                for j in 0..<cols {
                    if j == s { continue }
                    d[i][j] -= d[r][j] * d[i][s] * k
                }
            }

            // Scale pivot row
            for j in 0..<cols {
                d[r][j] *= k
            }

            // Scale pivot column (other rows)
            for i in 0..<rows {
                if i == r { continue }
                d[i][s] *= -k
            }

            d[r][s] = k

            swap(&bIndices[r], &nIndices[s])
        }

        func find(_ pIdx: Int) -> Bool {
            while true {
                var bestS: Int? = nil
                var bestVal: (Double, Int32) = (INF, Int32.max)

                // Choose entering variable
                for i in 0...n {
                    if pIdx != 0 || nIndices[i] != -1 {
                        let val = d[m + pIdx][i]
                        let key: (Double, Int32) = (val, nIndices[i])
                        if bestS == nil ||
                            key.0 < bestVal.0 - EPS ||
                            (abs(key.0 - bestVal.0) <= EPS && key.1 < bestVal.1) {
                            bestS = i
                            bestVal = key
                        }
                    }
                }

                guard let s = bestS else { return false }

                if d[m + pIdx][s] > -EPS {
                    return true
                }

                // Choose leaving variable
                var bestR: Int? = nil
                var bestRKey: (Double, Int32) = (INF, Int32.max)

                for i in 0..<m {
                    if d[i][s] > EPS {
                        let ratio = d[i][n + 1] / d[i][s]
                        let key: (Double, Int32) = (ratio, bIndices[i])
                        if bestR == nil ||
                            key.0 < bestRKey.0 - EPS ||
                            (abs(key.0 - bestRKey.0) <= EPS && key.1 < bestRKey.1) {
                            bestR = i
                            bestRKey = key
                        }
                    }
                }

                guard let r = bestR else {
                    // Unbounded
                    return false
                }

                pivot(r, s)
            }
        }

        // Phase I: choose row with min RHS as split_r
        var splitR = 0
        var minVal = d[0][n + 1]
        if m > 1 {
            for i in 1..<m {
                if d[i][n + 1] < minVal {
                    minVal = d[i][n + 1]
                    splitR = i
                }
            }
        }

        if d[splitR][n + 1] < -EPS {
            pivot(splitR, n)
            if !find(1) || d[m + 1][n + 1] < -EPS {
                // Infeasible
                return (-INF, nil)
            }

            // Eliminate artificial variable from basis
            for i in 0..<m {
                if bIndices[i] == -1 {
                    var bestS = 0
                    var bestKey: (Double, Int32) = (d[i][0], nIndices[0])
                    for j in 1..<n {
                        let key: (Double, Int32) = (d[i][j], nIndices[j])
                        if key.0 < bestKey.0 - EPS ||
                            (abs(key.0 - bestKey.0) <= EPS && key.1 < bestKey.1) {
                            bestS = j
                            bestKey = key
                        }
                    }
                    pivot(i, bestS)
                }
            }
        }

        // Phase II
        if find(0) {
            var x = Array(repeating: 0.0, count: n)
            for i in 0..<m {
                let idx = bIndices[i]
                if idx >= 0 && Int(idx) < n {
                    x[Int(idx)] = d[i][n + 1]
                }
            }
            var sumVal = 0.0
            for i in 0..<n {
                sumVal += c[i] * x[i]
            }
            return (sumVal, x)
        }

        return (-INF, nil)
    }

    func solveILP_BNB(initialA: [[Double]], objCoeffs: [Double]) -> Int64 {
        var bestVal = INF
        var stack: [[[Double]]] = []
        stack.append(initialA)

        while let currentA = stack.popLast() {
            let (val, xOpt) = simplex(lhs: currentA, c: objCoeffs)

            if val == -INF || val >= bestVal - EPS {
                continue
            }

            guard let x = xOpt else { continue }

            var fractionalIdx: Int? = nil
            var fractionalVal: Double = 0.0

            for (i, xv) in x.enumerated() {
                if abs(xv - xv.rounded()) > EPS {
                    fractionalIdx = i
                    fractionalVal = xv
                    break
                }
            }

            if let idx = fractionalIdx {
                let floorV = floor(fractionalVal)
                let nCols = currentA[0].count

                // x_idx <= floorV
                var row1 = Array(repeating: 0.0, count: nCols)
                row1[idx] = 1.0
                row1[nCols - 1] = floorV
                var a1 = currentA
                a1.append(row1)
                stack.append(a1)

                // x_idx >= ceilV  -> -x_idx <= -ceilV
                let ceilV = ceil(fractionalVal)
                var row2 = Array(repeating: 0.0, count: nCols)
                row2[idx] = -1.0
                row2[nCols - 1] = -ceilV
                var a2 = currentA
                a2.append(row2)
                stack.append(a2)
            } else if val < bestVal {
                bestVal = val
            }
        }

        if bestVal == INF {
            return 0
        } else {
            return Int64(bestVal.rounded())
        }
    }

    // MARK: - LP setup per Machine

    func solveLP(_ machine: MachineForPart2) -> Int64 {
        let numGoals = machine.goalCounters.count
        let numButtons = machine.buttonMasks.count

        let rows = 2 * numGoals + numButtons
        let cols = numButtons + 1  // last is RHS

        var matrix = Array(
            repeating: Array(repeating: 0.0, count: cols),
            count: rows
        )

        // Last numButtons rows: -x_j <= 0  -> rows from bottom
        // Rust: for (j, row) in matrix.iter_mut().rev().take(num_buttons).enumerate() { row[j] = -1.0; }
        for (j, rowIndex) in (0..<rows).reversed().prefix(numButtons).enumerated() {
            matrix[rowIndex][j] = -1.0
        }

        // For each button / mask
        for (j, mask) in machine.buttonMasks.enumerated() {
            for i in 0..<numGoals {
                if ((mask >> UInt32(i)) & 1) == 1 {
                    matrix[i][j] = 1.0
                    matrix[i + numGoals][j] = -1.0
                }
            }
        }

        // RHS for goals
        for i in 0..<numGoals {
            let val = Double(machine.goalCounters[i])
            matrix[i][cols - 1] = val
            matrix[i + numGoals][cols - 1] = -val
        }

        // Objective: minimize sum x_j
        let objCoeffs = Array(repeating: 1.0, count: numButtons)
        return solveILP_BNB(initialA: matrix, objCoeffs: objCoeffs)
    }

    func solvePart2(str: String) -> Int {
        let machines = parse(str)
        return Int(solveAll(machines))
    }
}

