import Foundation

public class Puzzle_2022_14: PuzzleBaseClass {
    public func solve() {
        let part1 = solvePart1()
        print("Part 1 solution: \(part1)")

        let part2 = solvePart2()
        print("Part 2 solution: \(part2)")
    }

    public func solvePart1() -> Int {
        solvePart1(str: Puzzle_Input.final)
    }

    public func solvePart2() -> Int {
        solvePart2(str: Puzzle_Input.final)
    }

    private func parse(_ str: String) -> (CharacterGrid, Int) {
        var grid = initializeEmptyCharacterGrid(height: 500, width: 1_000)
        let lines = str.parseIntoStringArray()
        var maxY = Int.min
        for line in lines {
            let arr = line.parseIntoStringArray(separator: " ").filter { $0 != "->" }.map { $0.point2D }
            for idx in 1..<arr.count {
                let p1 = arr[idx - 1]
                let p2 = arr[idx]
                if p1.x == p2.x {
                    let y1 = min(p1.y, p2.y)
                    let y2 = max(p1.y, p2.y)
                    for y in y1...y2 {
                        grid[y][p1.x] = "#"
                        if y > maxY {
                            maxY = y
                        }
                    }
                } else {
                    if p1.y > maxY {
                        maxY = p1.y
                    }

                    let x1 = min(p1.x, p2.x)
                    let x2 = max(p1.x, p2.x)
                    for x in x1...x2 {
                        grid[p1.y][x] = "#"
                    }
                }
            }
        }

        return (grid, maxY)
    }

    private func lookBelowSand(grid: CharacterGrid, point: Point2D) -> [Character] {
        if point.y == grid.height - 1 {
            return []
        }

        return [grid[point.y + 1][point.x - 1], grid[point.y + 1][point.x], grid[point.y + 1][point.x + 1]]
    }

    private func sandCanMove(_ arr: [Character]) -> Bool {
        if arr.count != 3 {
            return false
        }

        return arr[0] == "." || arr[1] == "." || arr[2] == "."
    }

    private func solvePart1(str: String) -> Int {
        var (grid, _) = parse(str)
        var sand = 0
        var leaveLoop = false
        while !leaveLoop {
            sand += 1
            var sandPoint = Point2D(x: 500, y: 0)
            var moves = lookBelowSand(grid: grid, point: sandPoint)
            while sandCanMove(moves) {
                if moves[1] == "." {
                    sandPoint.y += 1
                } else if moves[0] == "." {
                    sandPoint.y += 1
                    sandPoint.x -= 1
                } else {
                    sandPoint.y += 1
                    sandPoint.x += 1
                }

                moves = lookBelowSand(grid: grid, point: sandPoint)
                if moves.isEmpty {
                    leaveLoop = true
                }
            }

            grid[sandPoint.y][sandPoint.x] = "o"
        }

        return sand - 1
    }

    private func solvePart2(str: String) -> Int {
        var (grid, maxY) = parse(str)

        for x in 0..<1_000 {
            grid[maxY + 2][x] = "#"
        }

        var sand = 0
        var leaveLoop = false
        while !leaveLoop {
            sand += 1
            var sandPoint = Point2D(x: 500, y: 0)
            var moves = lookBelowSand(grid: grid, point: sandPoint)
            while sandCanMove(moves) {
                if moves[1] == "." {
                    sandPoint.y += 1
                } else if moves[0] == "." {
                    sandPoint.y += 1
                    sandPoint.x -= 1
                } else {
                    sandPoint.y += 1
                    sandPoint.x += 1
                }

                moves = lookBelowSand(grid: grid, point: sandPoint)
                if moves.isEmpty {
                    leaveLoop = true
                }
            }

            grid[sandPoint.y][sandPoint.x] = "o"
            if sandPoint.y == 0 && sandPoint.x == 500 {
                leaveLoop = true
            }
        }

        return sand
    }
}

