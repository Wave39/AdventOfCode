import Foundation

public class Puzzle_2022_02: PuzzleBaseClass {
    private enum Move {
        case rock
        case paper
        case scissors

        static func parse(_ str: String) -> Move {
            (str == "A" || str == "X") ? .rock : (str == "B" || str == "Y") ? .paper : .scissors
        }

        func score() -> Int {
            self == .rock ? 1 : self == .paper ? 2 : 3
        }
    }

    private enum GameResult {
        case win
        case lose
        case draw

        static func parse(_ str: String) -> GameResult {
            str == "X" ? .lose : str == "Y" ? .draw : .win
        }

        func score() -> Int {
            self == .win ? 6 : self == .draw ? 3 : 0
        }
    }

    public func solve() {
        let part1 = solvePart1()
        print("Part 1 solution: \(part1)")

        let part2 = solvePart2()
        print("Part 2 solution: \(part2)")
    }

    public func solvePart1() -> Int {
        solvePart1(str: Puzzle_Input.final)
    }

    public func solvePart2() -> Int {
        solvePart2(str: Puzzle_Input.final)
    }

    private func gameScorePart1(opponentMove: Move, myMove: Move) -> Int {
        if myMove == .rock {
            if opponentMove == .rock {
                return Move.rock.score() + GameResult.draw.score()
            } else if opponentMove == .paper {
                return Move.rock.score() + GameResult.lose.score()
            } else {
                return Move.rock.score() + GameResult.win.score()
            }
        } else if myMove == .paper {
            if opponentMove == .rock {
                return Move.paper.score() + GameResult.win.score()
            } else if opponentMove == .paper {
                return Move.paper.score() + GameResult.draw.score()
            } else {
                return Move.paper.score() + GameResult.lose.score()
            }
        } else {
            if opponentMove == .rock {
                return Move.scissors.score() + GameResult.lose.score()
            } else if opponentMove == .paper {
                return Move.scissors.score() + GameResult.win.score()
            } else {
                return Move.scissors.score() + GameResult.draw.score()
            }
        }
    }

    private func gameScorePart2(opponentMove: Move, desiredResult: GameResult) -> Int {
        if opponentMove == .rock {
            if desiredResult == .lose {
                return Move.scissors.score() + GameResult.lose.score()
            } else if desiredResult == .draw {
                return Move.rock.score() + GameResult.draw.score()
            } else {
                return Move.paper.score() + GameResult.win.score()
            }
        } else if opponentMove == .paper {
            if desiredResult == .lose {
                return Move.rock.score() + GameResult.lose.score()
            } else if desiredResult == .draw {
                return Move.paper.score() + GameResult.draw.score()
            } else {
                return Move.scissors.score() + GameResult.win.score()
            }
        } else {
            if desiredResult == .lose {
                return Move.paper.score() + GameResult.lose.score()
            } else if desiredResult == .draw {
                return Move.scissors.score() + GameResult.draw.score()
            } else {
                return Move.rock.score() + GameResult.win.score()
            }
        }
    }

    private func solvePart1(str: String) -> Int {
        let lines = str.parseIntoStringArray()
        var score = 0
        for line in lines {
            let arr = line.parseIntoStringArray(separator: " ")
            score += gameScorePart1(opponentMove: Move.parse(arr[0]), myMove: Move.parse(arr[1]))
        }

        return score
    }

    private func solvePart2(str: String) -> Int {
        let lines = str.parseIntoStringArray()
        var score = 0
        for line in lines {
            let arr = line.parseIntoStringArray(separator: " ")
            score += gameScorePart2(opponentMove: Move.parse(arr[0]), desiredResult: GameResult.parse(arr[1]))
        }

        return score
    }
}

