import Foundation

public class Puzzle_2024_06: PuzzleBaseClass {
    public func solve() {
        let part1 = solvePart1()
        print("Part 1 solution: \(part1)")

        let part2 = solvePart2()
        print("Part 2 solution: \(part2)")
    }

    public func solvePart1() -> Int {
        solvePart1(str: Puzzle_Input.final)
    }

    public func solvePart2() -> Int {
        solvePart2(str: Puzzle_Input.final)
    }

    private func solvePart1(str: String) -> Int {
        let grid = str.parseIntoCharacterMatrix()
        var direction = CompassDirection.North
        var position = grid.findInstancesOf("^").first!
        var visitedPositions = Set<Point2D>()
        visitedPositions.insert(position)
        while true {
            if grid.isNextStepOffGrid(point: position, direction: direction) {
                break
            }

            let directionalCharacter = grid.getDirectionalCharacter(point: position, direction: direction)
            if directionalCharacter == "#" {
                direction = direction.TurnRight()
            } else {
                position = position.moveForwardFromTopLeftOrigin(direction: direction)
                visitedPositions.insert(position)
            }
        }

        return visitedPositions.count
    }

    struct PositionAndDirection: Hashable {
        var position: Point2D
        var direction: CompassDirection
    }

    private func solvePart2(str: String) -> Int {
        var retval = 0
        let originalGrid = str.parseIntoCharacterMatrix()
        let testPositions = originalGrid.getAllPointsMatchingCharacter(".")
        for testPosition in testPositions {
            var newGrid = originalGrid
            newGrid.setCharacterAtPoint(testPosition, "#")
            var direction = CompassDirection.North
            var position = newGrid.findInstancesOf("^").first!
            var visitedPositions = Set<PositionAndDirection>()
            var loopFormed = false
            visitedPositions.insert(PositionAndDirection(position: position, direction: direction))
            while true {
                if newGrid.isNextStepOffGrid(point: position, direction: direction) {
                    break
                }

                let directionalCharacter = newGrid.getDirectionalCharacter(point: position, direction: direction)
                if directionalCharacter == "#" {
                    direction = direction.TurnRight()
                } else {
                    position = position.moveForwardFromTopLeftOrigin(direction: direction)
                    if visitedPositions.contains(PositionAndDirection(position: position, direction: direction)) {
                        loopFormed = true
                        break
                    }

                    visitedPositions.insert(PositionAndDirection(position: position, direction: direction))
                }
            }

            if loopFormed {
                retval += 1
            }
        }

        return retval
    }
}

