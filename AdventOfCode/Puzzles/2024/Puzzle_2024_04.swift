import Foundation

public class Puzzle_2024_04: PuzzleBaseClass {
    public func solve() {
        let part1 = solvePart1()
        print("Part 1 solution: \(part1)")

        let part2 = solvePart2()
        print("Part 2 solution: \(part2)")
    }

    public func solvePart1() -> Int {
        solvePart1(str: Puzzle_Input.final)
    }

    public func solvePart2() -> Int {
        solvePart2(str: Puzzle_Input.final)
    }

    private func solvePart1(str: String) -> Int {
        var retval = 0
        let grid = str.parseIntoCharacterMatrix()
        let gridRows = grid.count
        let gridColumns = grid[0].count
        for row in 0..<gridRows {
            for col in 0..<gridColumns {
                if grid[row][col] == "X" {
                    let pt = Point2D(x: col, y: row)
                    let paths = pt.walkingPaths(rows: gridRows, columns: gridColumns, stepCount: 4)
                    for path in paths {
                        if grid.characterAtPoint(path[1]) == "M" && grid.characterAtPoint(path[2]) == "A" && grid.characterAtPoint(path[3]) == "S" {
                            retval += 1
                        }
                    }
                }
            }
        }

        return retval
    }

    private func solvePart2(str: String) -> Int {
        var retval = 0
        let grid = str.parseIntoCharacterMatrix()
        let gridRows = grid.count
        let gridColumns = grid[0].count
        for row in 1..<(gridRows - 1) {
            for col in 1..<(gridColumns - 1) {
                if grid[row][col] == "A" {
                    let a1 = grid[row - 1][col - 1]
                    let a2 = grid[row + 1][col + 1]
                    let b1 = grid[row - 1][col + 1]
                    let b2 = grid[row + 1][col - 1]
                    if ((a1 == "M" && a2 == "S") || (a1 == "S" && a2 == "M")) && ((b1 == "M" && b2 == "S") || (b1 == "S" && b2 == "M")) {
                        retval += 1
                    }
                }
            }
        }

        return retval
    }
}

