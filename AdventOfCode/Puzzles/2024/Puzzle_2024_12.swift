import Foundation

public class Puzzle_2024_12: PuzzleBaseClass {
    public func solve() {
        let part1 = solvePart1()
        print("Part 1 solution: \(part1)")

        let part2 = solvePart2()
        print("Part 2 solution: \(part2)")
    }

    public func solvePart1() -> Int {
        solvePart1(str: Puzzle_Input.final)
    }

    public func solvePart2() -> Int {
        solvePart2(str: Puzzle_Input.final)
    }

    func findRegion(matrix: inout [[Character]], start: Point2D) -> [Point2D] {
        let rows = matrix.count
        let cols = matrix[0].count
        let startChar = matrix.characterAtPoint(start)

        // Directions for moving in the matrix (up, down, left, right)
        let directions = [(-1, 0), (1, 0), (0, -1), (0, 1)]

        // To store the region's cells
        var region = [Point2D]()

        // Helper function for DFS
        func dfs(_ r: Int, _ c: Int) {
            // Boundary and character check
            guard r >= 0, r < rows, c >= 0, c < cols, matrix[r][c] == startChar else { return }

            // Mark the cell as visited (replace with a sentinel value, e.g., '.')
            matrix[r][c] = "."

            // Add the cell to the region
            region.append(Point2D(x: c, y: r))

            // Visit all neighbors
            for (dr, dc) in directions {
                dfs(r + dr, c + dc)
            }
        }

        // Start DFS from the given starting point
        dfs(start.y, start.x)

        return region
    }

    private func solvePart1(str: String) -> Int {
        var garden = str.parseIntoCharacterMatrix()
        var point = garden.findFirstLetter()
        var totalPrice = 0
        while point != nil {
            //let gardenCharacter = garden.characterAtPoint(point!)
            let region = findRegion(matrix: &garden, start: point!)
            let regionArea = region.count

            var fenceSegments = 0

            // vertical slices
            let minX = region.map { $0.x }.min()!
            let maxX = region.map { $0.x }.max()!
            for x in minX...maxX {
                let yValues = region.filter { $0.x == x }.map { $0.y }.sorted()
                fenceSegments += (2 + yValues.countIntegerGaps() * 2)
            }

            // horizontal slices
            let minY = region.map { $0.y }.min()!
            let maxY = region.map { $0.y }.max()!
            for y in minY...maxY {
                let xValues = region.filter { $0.y == y }.map { $0.x }.sorted()
                fenceSegments += (2 + xValues.countIntegerGaps() * 2)
            }

            //print("\(gardenCharacter) \(regionArea) \(fenceSegments)")
            totalPrice += (regionArea * fenceSegments)

            point = garden.findFirstLetter()
        }

        return totalPrice
    }

    // https://github.com/gereons/AoC2024/blob/main/Sources/Day12/Day12.swift

    private func edges(of region: Set<Point2D>) -> Int {
        let minX = region.map { $0.x }.min()!
        let maxX = region.map { $0.x }.max()!
        let minY = region.map { $0.y }.min()!
        let maxY = region.map { $0.y }.max()!

        var edges = 0
        // top to bottom
        for y in minY...maxY {
            let points = region.filter { !region.contains(Point2D(x: $0.x, y: y - 1)) && $0.y == y }.sorted(by: { $0.x < $1.x })
            let chunks = points.chunked { p1, p2 in p2.x - p1.x == 1 }
            edges += chunks.count
        }

        // bottom to top
        for y in (minY...maxY).reversed() {
            let points = region.filter { !region.contains(Point2D(x: $0.x, y: y + 1)) && $0.y == y }.sorted(by: { $0.x < $1.x })
            let chunks = points.chunked { p1, p2 in p2.x - p1.x == 1 }
            edges += chunks.count
        }

        // left to right
        for x in minX...maxX {
            let points = region.filter { !region.contains(Point2D(x: x - 1, y: $0.y)) && $0.x == x }.sorted(by: { $0.y < $1.y })
            let chunks = points.chunked { p1, p2 in p2.y - p1.y == 1 }
            edges += chunks.count
        }

        // right to left
        for x in (minX...maxX).reversed() {
            let points = region.filter { !region.contains(Point2D(x: x + 1, y: $0.y)) && $0.x == x }.sorted(by: { $0.y < $1.y })
            let chunks = points.chunked { p1, p2 in p2.y - p1.y == 1 }
            edges += chunks.count
        }

        return edges
    }

    private func solvePart2(str: String) -> Int {
        var garden = str.parseIntoCharacterMatrix()
        var point = garden.findFirstLetter()
        var totalPrice = 0
        while point != nil {
            let region = findRegion(matrix: &garden, start: point!)
            let regionArea = region.count
            let edgeCount = edges(of: Set(region))
            totalPrice += (regionArea * edgeCount)
            point = garden.findFirstLetter()
        }

        return totalPrice
    }
}

