import Foundation

public class Puzzle_2024_25: PuzzleBaseClass {
    public func solve() {
        let part1 = solvePart1()
        print("Part 1 solution: \(part1)")
    }

    public func solvePart1() -> Int {
        solvePart1(str: Puzzle_Input.final)
    }

    private func solvePart1(str: String) -> Int {
        let lines = str.parseIntoStringArray()
        var columns: [[Character]] = Array(repeating: [], count: 5)
        var keys: [[Int]] = []
        var locks: [[Int]] = []
        for line in lines {
            for (idx, char) in line.enumerated() {
                columns[idx].append(char)
            }

            if columns[0].count == 7 {
                let heights = columns.map { $0.filter { $0 == "#" }.count - 1 }
                if columns[0][0] == "#" {
                    locks.append(heights)
                } else {
                    keys.append(heights)
                }

                columns = Array(repeating: [], count: 5)
            }
        }

        var retval = 0
        for key in keys {
            for lock in locks {
                let arr = zip(key, lock).map(+)
                if arr.filter({ $0 > 5 }).isEmpty {
                    retval += 1
                }
            }
        }

        return retval
    }
}

