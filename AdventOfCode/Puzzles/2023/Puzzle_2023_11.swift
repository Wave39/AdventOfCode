import Foundation

public class Puzzle_2023_11: PuzzleBaseClass {
    public func solve() {
        let part1 = solvePart1()
        print("Part 1 solution: \(part1)")

        let part2 = solvePart2()
        print("Part 2 solution: \(part2)")
    }

    public func solvePart1() -> Int {
        solvePart1(str: Puzzle_Input.final)
    }

    public func solvePart2() -> Int {
        solvePart2(str: Puzzle_Input.final)
    }

    private func solve(str: String, expansion: Int) -> Int {
        let grid = str.parseIntoCharacterMatrix()
        var expandedColumns = Set<Int>()
        var expandedRows = Set<Int>()
        for row in 0..<grid.height {
            if !grid[row].contains("#") {
                expandedRows.insert(row)
            }
        }

        for column in 0..<grid.width {
            let arr = grid.map { $0[column] }
            if !arr.contains("#") {
                expandedColumns.insert(column)
            }
        }

        let galaxies = grid.findInstancesOf("#")
        var retval = 0
        for i in 0..<(galaxies.count - 1) {
            for j in (i + 1)..<galaxies.count {
                retval += galaxies[i].manhattanDistanceTo(pt: galaxies[j])

                let minRow = min(galaxies[i].y, galaxies[j].y)
                let maxRow = max(galaxies[i].y, galaxies[j].y)
                for r in minRow...maxRow {
                    if expandedRows.contains(r) {
                        retval += expansion
                    }
                }

                let minColumn = min(galaxies[i].x, galaxies[j].x)
                let maxColumn = max(galaxies[i].x, galaxies[j].x)
                for c in minColumn...maxColumn {
                    if expandedColumns.contains(c) {
                        retval += expansion
                    }
                }
            }
        }

        return retval
    }

    private func solvePart1(str: String) -> Int {
        return solve(str: str, expansion: 1)
    }

    private func solvePart2(str: String) -> Int {
        return solve(str: str, expansion: 999_999)
    }
}

