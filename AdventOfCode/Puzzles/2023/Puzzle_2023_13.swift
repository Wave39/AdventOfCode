import Foundation

public class Puzzle_2023_13: PuzzleBaseClass {
    public func solve() {
        let part1 = solvePart1()
        print("Part 1 solution: \(part1)")

        let part2 = solvePart2()
        print("Part 2 solution: \(part2)")
    }

    public func solvePart1() -> Int {
        solvePart1(str: Puzzle_Input.final)
    }

    public func solvePart2() -> Int {
        solvePart2(str: Puzzle_Input.final)
    }

    private func findReflectionPoint(_ arr: [Int], pointToIgnore: Int?) -> Int? {
        for testPoint in (1...arr.count - 1) {
            let testCount = min(testPoint, arr.count - testPoint)
            var rowOk = true
            for idx in 0..<testCount {
                if arr[testPoint - idx - 1] != arr[testPoint + idx] {
                    rowOk = false
                    break
                }
            }

            if rowOk {
                if let pointToIgnore {
                    if testPoint != pointToIgnore {
                        return testPoint
                    }
                } else {
                    return testPoint
                }
            }
        }

        return nil
    }

    private func processGrid(_ str: String) -> ([Int], [Int]) {
        let grid = str.parseIntoCharacterMatrix()
        var hValues = [Int]()
        for row in 0..<grid.height {
            hValues.append(grid[row].hashValue)
        }
        
        var vValues = [Int]()
        for column in 0..<grid.width {
            let columnString = String(grid.map { $0[column] })
            vValues.append(columnString.hashValue)
        }

        return (hValues, vValues)
    }

    private func solvePart1(str: String) -> Int {
        let str = str.replacingOccurrences(of: "\n\n", with: "~")
        let arr = str.parseIntoStringArray(separator: "~")
        var retval = 0
        for pattern in arr {
            let (hValues, vValues) = processGrid(pattern)
            if let hReflectionPoint = findReflectionPoint(hValues, pointToIgnore: nil) {
                retval += hReflectionPoint * 100
            } else if let vReflectionPoint = findReflectionPoint(vValues, pointToIgnore: nil) {
                retval += vReflectionPoint
            }
        }

        return retval
    }

    private func solvePart2(str: String) -> Int {
        let str = str.replacingOccurrences(of: "\n\n", with: "~")
        let arr = str.parseIntoStringArray(separator: "~")
        var retval = 0
        for pattern in arr {
            let (hValuesOriginal, vValuesOriginal) = processGrid(pattern)
            let hReflectionPointOriginal = findReflectionPoint(hValuesOriginal, pointToIgnore: nil)
            let vReflectionPointOriginal = findReflectionPoint(vValuesOriginal, pointToIgnore: nil)

            var hReflectionPoint: Int? = nil
            var vReflectionPoint: Int? = nil
            var newPattern: String
            for idx in 0..<pattern.count {
                if pattern[idx] == "\n" {
                    continue
                } else if pattern[idx] == "." {
                    newPattern = pattern.replace(index: idx, newChar: "#")
                } else {
                    newPattern = pattern.replace(index: idx, newChar: ".")
                }

                let (hValues, vValues) = processGrid(newPattern)
                hReflectionPoint = findReflectionPoint(hValues, pointToIgnore: hReflectionPointOriginal)
                if hReflectionPoint != nil {
                    break
                }

                vReflectionPoint = findReflectionPoint(vValues, pointToIgnore: vReflectionPointOriginal)
                if vReflectionPoint != nil {
                    break
                }
            }

            if let hReflectionPoint {
                retval += hReflectionPoint * 100
            } else if let vReflectionPoint {
                retval += vReflectionPoint
            }
        }

        return retval
    }
}

