import Foundation

class Puzzle_2015_18: PuzzleBaseClass {

    func solve() {
        let (part1, part2) = solveBothParts()
        print("Part 1 solution: \(part1)")
        print("Part 2 solution: \(part2)")
    }

    func solveBothParts() -> (Int, Int) {
        let puzzleInputLineArray = PuzzleInput.final.parseIntoStringArray()

        let rows = puzzleInputLineArray.count
        let columns = puzzleInputLineArray[0].count
        let numberOfSteps = 100

        func getInitialConfiguration() -> [[Bool]] {
            var config = Array(repeating: Array(repeating: false, count: columns), count: rows)

            for r in 0..<rows {
                let line = puzzleInputLineArray[r]
                for c in 0..<columns {
                    if line[c] == "#" {
                        config[r][c] = true
                    }
                }
            }

            return config
        }

        func countLightsOn(arr: [[Bool]]) -> Int {
            var ctr = 0
            for r in 0..<rows {
                for c in 0..<columns {
                    if arr[r][c] {
                        ctr += 1
                    }
                }
            }

            return ctr
        }

        func printGrid(arr: [[Bool]]) {
            var s = ""
            for r in 0..<rows {
                for c in 0..<columns {
                    s += (arr[r][c] ? "#" : ".")
                }

                s += "\n"
            }

            print(s)
        }

        func isCorner(x: Int, y: Int) -> Bool {
            (x == 0 && y == 0) || (x == (rows - 1) && y == 0) ||
                (x == 0 && y == (columns - 1)) || (x == (rows - 1) && y == (columns - 1))
        }

        func neighborLightsOn(arr: [[Bool]], row: Int, col: Int, cornerLightsStuckOn: Bool) -> Int {
            var ctr = 0
            for x in (row - 1)...(row + 1) {
                for y in (col - 1)...(col + 1) {
                    if x != row || y != col {
                        if cornerLightsStuckOn && isCorner(x: x, y: y) {
                            ctr += 1
                        } else if x >= 0 && x < rows && y >= 0 && y < columns {
                            if arr[x][y] {
                                ctr += 1
                            }
                        }
                    }
                }
            }

            return ctr
        }

        func getNextConfiguration(arr: [[Bool]], cornerLightsStuckOn: Bool) -> [[Bool]] {
            var nextConfiguration = Array(repeating: Array(repeating: false, count: columns), count: rows)
            for r in 0..<rows {
                for c in 0..<columns {
                    let onCtr = neighborLightsOn(arr: arr, row: r, col: c, cornerLightsStuckOn: cornerLightsStuckOn)
                    if arr[r][c] {
                        if onCtr < 2 || onCtr > 3 {
                            nextConfiguration[r][c] = false
                        } else {
                            nextConfiguration[r][c] = true
                        }
                    } else {
                        if onCtr == 3 {
                            nextConfiguration[r][c] = true
                        } else {
                            nextConfiguration[r][c] = false
                        }
                    }
                }
            }

            if cornerLightsStuckOn {
                nextConfiguration[0][0] = true
                nextConfiguration[0][columns - 1] = true
                nextConfiguration[rows - 1][0] = true
                nextConfiguration[rows - 1][columns - 1] = true
            }

            return nextConfiguration
        }

        var part1Configuration = getInitialConfiguration()
        for _ in 1...numberOfSteps {
            part1Configuration = getNextConfiguration(arr: part1Configuration, cornerLightsStuckOn: false)
        }

        let part1 = countLightsOn(arr: part1Configuration)

        var part2Configuration = getInitialConfiguration()
        for _ in 1...numberOfSteps {
            part2Configuration = getNextConfiguration(arr: part2Configuration, cornerLightsStuckOn: true)
        }

        let part2 = countLightsOn(arr: part2Configuration)

        return (part1, part2)
    }

}

